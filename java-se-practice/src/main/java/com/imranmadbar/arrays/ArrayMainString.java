package com.imranmadbar.arrays;

public class ArrayMainString {

	public static void main(String[] args) {

		String[] names = { "John", "Jade", "Love", "Allen" };

		System.out.println(names[0]); // John

		System.out.println(names[1]); // Jade

		System.out.println(names[2]); // Love

		System.out.println(names[3]);

		for (int i = 0; i < names.length; i++) {
			System.out.println(names[i]);
		}

	}

}
