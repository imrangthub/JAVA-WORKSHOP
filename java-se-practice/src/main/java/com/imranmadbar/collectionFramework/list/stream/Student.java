package com.imranmadbar.collectionFramework.list.stream;

public class Student {
	

	private String info;
	
	
	public Student() {}
	
	public Student(String info) {
		this.info = info;
	}


	public String getInfo() {
		return info;
	}

	public void setInfo(String info) {
		this.info = info;
	}

	@Override
	public String toString() {
		return "Student [info=" + info + "]";
	}
	
	
	
	
	
	
	

}
